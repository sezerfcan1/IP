        public static void ResmiKaydet(PictureBox picSol, PictureBox picSag)
        {
            SaveFileDialog saveFileDialog1 = new SaveFileDialog();
            saveFileDialog1.Filter = "Jpeg Resmi|*.jpg|Bitmap Resmi|*.bmp|Gif Resmi|*.gif";
            saveFileDialog1.Title = "Resmi Kaydet";
            saveFileDialog1.ShowDialog();
            try
            {
                if (saveFileDialog1.FileName != "") //Dosya adı boş değilse kaydedecek.
                {
                    // FileStream nesnesi ile kayıtı gerçekleştirecek.
                    FileStream DosyaAkisi = (FileStream)saveFileDialog1.OpenFile();
                    switch (saveFileDialog1.FilterIndex)
                    {
                        case 1:
                            picSag.Image.Save(DosyaAkisi, System.Drawing.Imaging.ImageFormat.Jpeg);
                            break;
                        case 2:
                            picSag.Image.Save(DosyaAkisi, System.Drawing.Imaging.ImageFormat.Bmp);
                            break;
                        case 3:
                            picSag.Image.Save(DosyaAkisi, System.Drawing.Imaging.ImageFormat.Gif);
                            break;
                    }
                    DosyaAkisi.Close();
                }
            }
            catch (Exception)
            {
                MessageBox.Show("Resimde hiçbir değişiklik yapılmadı !");
            }
        }

